def process_entity(entity: str, state: StateManager) -> dict:
    """Agentic pipeline for one entity. LLM decides at every gate."""

    start = datetime.now()
    audit = {"canonical_entity": entity, "steps": {}, "llm_calls": 0, "api_calls": 0}
    row = {"canonical_entity": entity}

    print(f"\n{'─'*60}")
    print(f"  {entity.upper()}")
    print(f"{'─'*60}")

    # ── GATE 1: Pre-scrape scope validation (LLM) ────────────────────
    print("  ► Pre-scrape validation")
    val = validate_is_restaurant(entity)
    audit["llm_calls"] += 1
    row["is_restaurant"] = val.get("is_restaurant", True)
    state.log_validation({"canonical_entity": entity, "step": "pre_scrape",
                          "decision": row["is_restaurant"], "reason": val.get("reason", "")})

    if not row["is_restaurant"]:
        print(f"     ✗ Out of scope: {val.get('reason')}")
        row.update({k: None for k in [
            "gm_rating", "gm_review_count", "gm_address", "gm_phone",
            "gm_website", "gm_category", "ta_rating", "ta_review_count",
            "ta_url", "ig_handle", "ig_followers", "ig_posts",
            "ig_engagement_rate", "ig_bio", "fb_handle", "fb_page_likes",
            "fb_post_engagement", "has_wikipedia",
        ]})
        row.update(compute_derived(row))
        row["overall_confidence"] = 0.0
        row["scrape_timestamp"] = datetime.now().isoformat()
        state.save_entity(row)
        audit["duration_s"] = (datetime.now() - start).total_seconds()
        state.log_audit(audit)
        return row

    # ── STEP 1: Google Maps (SerpApi) ─────────────────────────────────
    print("  ► Google Maps (SerpApi)")
    gm = scrape_serpapi(entity)
    audit["api_calls"] += 1
    audit["steps"]["serpapi"] = gm["status"]

    row["gm_rating"]       = gm["gm_rating"]
    row["gm_review_count"] = gm["gm_review_count"]
    row["gm_address"]      = gm["gm_address"]
    row["gm_phone"]        = gm["gm_phone"]
    row["gm_website"]      = gm["gm_website"]
    row["gm_category"]     = gm["gm_category"]

    # ── GATE 2: Category validation (LLM) ─────────────────────────────
    if gm["gm_category"] and gm["status"] == "success":
        print("  ► Category validation")
        cat_val = validate_category(entity, str(gm["gm_category"]), str(gm["gm_address"]))
        audit["llm_calls"] += 1

        if not cat_val.get("is_restaurant", True):
            print(f"     ✗ Not a restaurant: {cat_val.get('reason')}")
            row["is_restaurant"] = False
            state.log_validation({"canonical_entity": entity, "step": "category",
                                  "decision": False, "reason": cat_val.get("reason", "")})
            # Save Google Maps data but skip social scraping
            row.update({k: None for k in [
                "ta_rating", "ta_review_count", "ta_url",
                "ig_handle", "ig_followers", "ig_posts",
                "ig_engagement_rate", "ig_bio",
                "fb_handle", "fb_page_likes", "fb_post_engagement",
                "has_wikipedia",
            ]})
            row.update(compute_derived(row))
            row["overall_confidence"] = 0.0
            row["scrape_timestamp"] = datetime.now().isoformat()
            state.save_entity(row)
            audit["duration_s"] = (datetime.now() - start).total_seconds()
            state.log_audit(audit)
            return row

    # ── STEP 2: Wikipedia ─────────────────────────────────────────────
    print("  ► Wikipedia")
    row["has_wikipedia"] = scrape_wikipedia(entity)

    # ── STEP 3: TripAdvisor ───────────────────────────────────────────
    print("  ► TripAdvisor")
    ta = scrape_tripadvisor(entity)
    row["ta_rating"]       = ta["ta_rating"]
    row["ta_review_count"] = ta["ta_review_count"]
    row["ta_url"]          = ta["ta_url"]

    # ── STEP 4: Website social link extraction ────────────────────────
    website_socials = {"website_ig": None, "website_fb": None}
    if gm["gm_website"]:
        print("  ► Website social links")
        website_socials = extract_social_from_website(gm["gm_website"])

    # ── STEP 5a: Instagram ────────────────────────────────────────────
    print("  ► Instagram")
    ig_handle, ig_source = resolve_ig_handle(entity, website_socials["website_ig"])
    ig_validated = False

    if ig_handle:
        print(f"     Handle: @{ig_handle} (source: {ig_source})")
        # LLM gate — validate before spending Apify credits
        hval = validate_handle(entity, ig_handle, "Instagram")
        audit["llm_calls"] += 1

        if not hval.get("plausible", True):
            print(f"     ✗ Rejected: {hval.get('reason')}")
            state.log_validation({"canonical_entity": entity, "step": "ig_handle",
                                  "decision": False, "reason": hval.get("reason", ""),
                                  "handle": ig_handle, "source": ig_source})
            ig_handle = None
            ig_source = "rejected"
        else:
            ig_validated = True
            print(f"     ✓ Handle validated")

    ig_data = scrape_instagram_apify(ig_handle) if ig_handle else {
        "ig_followers": 0, "ig_posts": 0, "ig_engagement_rate": 0.0,
        "ig_bio": "", "ig_scraper": "none",
    }
    if ig_handle: audit["api_calls"] += 1

    row["ig_handle"]          = ig_handle
    row["ig_handle_source"]   = ig_source
    row["ig_followers"]       = ig_data["ig_followers"]
    row["ig_posts"]           = ig_data["ig_posts"]
    row["ig_engagement_rate"] = ig_data["ig_engagement_rate"]
    row["ig_bio"]             = ig_data.get("ig_bio", "")

    # ── STEP 5b: Facebook ─────────────────────────────────────────────
    print("  ► Facebook")
    fb_handle, fb_source = resolve_fb_handle(entity, website_socials["website_fb"])
    fb_validated = False

    if fb_handle:
        print(f"     Handle: @{fb_handle} (source: {fb_source})")
        hval = validate_handle(entity, fb_handle, "Facebook")
        audit["llm_calls"] += 1

        if not hval.get("plausible", True):
            print(f"     ✗ Rejected: {hval.get('reason')}")
            state.log_validation({"canonical_entity": entity, "step": "fb_handle",
                                  "decision": False, "reason": hval.get("reason", ""),
                                  "handle": fb_handle, "source": fb_source})
            fb_handle = None
            fb_source = "rejected"
        else:
            fb_validated = True
            print(f"     ✓ Handle validated")

    fb_data = scrape_facebook_apify(fb_handle) if fb_handle else {
        "fb_page_likes": 0, "fb_post_engagement": 0.0, "fb_scraper": "none",
    }
    if fb_handle: audit["api_calls"] += 1

    row["fb_handle"]          = fb_handle
    row["fb_handle_source"]   = fb_source
    row["fb_page_likes"]      = fb_data["fb_page_likes"]
    row["fb_post_engagement"] = fb_data["fb_post_engagement"]

    # ── STEP 6: Data quality validation (LLM) ─────────────────────────
    print("  ► Data quality check")
    quality = validate_data_quality(entity, row)
    audit["llm_calls"] += 1
    n_issues = len(quality.get("issues", []))
    llm_conf = quality.get("confidence", 0.8)
    print(f"     Issues: {n_issues} | LLM confidence: {llm_conf}")
    if quality.get("summary"):
        print(f"     {quality['summary']}")

    # ── STEP 7: Derived features + confidence ─────────────────────────
    row.update(compute_derived(row))

    # Deterministic confidence: blend field completeness with LLM assessment
    field_score = row["data_source_count"] / 6.0  # max 6 sources
    row["overall_confidence"] = round(0.6 * field_score + 0.4 * llm_conf, 3)
    row["validation_flags"]   = n_issues
    row["scrape_timestamp"]   = datetime.now().isoformat()

    # ── SAVE (checkpoint) ─────────────────────────────────────────────
    state.save_entity(row)

    duration = (datetime.now() - start).total_seconds()
    audit["duration_s"]  = duration
    audit["confidence"]  = row["overall_confidence"]
    audit["flags"]       = n_issues
    state.log_audit(audit)

    print(f" Done ({duration:.0f}s) | conf={row['overall_confidence']} "
          f"| sources={row['data_source_count']} | flags={n_issues}")

    return row

print("Orchestrator defined.")

