from pipeline_state import PipelineState


def run_agent2_node(state: PipelineState) -> PipelineState:
    """LangGraph node: runs Agent 2 web enrichment and merges results into state."""
    errors = list(state.get("errors", []))

    # Build entity list from the global features computed by Agent 1.
    global_features = state.get("entity_features_global") or []
    entity_list = [r["canonical_entity"] for r in global_features if r.get("canonical_entity")]

    if not entity_list:
        errors.append("agent2: entity_features_global is empty — skipping web enrichment")
        return {**state, "errors": errors, "current_step": "agent2_skipped"}

    try:
        run_agent2(entity_list, fresh_start=False)
    except Exception as exc:
        errors.append(f"agent2: {exc}")
        return {**state, "errors": errors, "current_step": "agent2_error"}

    # Read the checkpoint CSV back and store as serialisable records.
    web_features: list = []
    try:
        import pandas as pd
        if os.path.exists(CHECKPOINT_FILE):
            web_features = pd.read_csv(CHECKPOINT_FILE, encoding="utf-8-sig").to_dict(orient="records")
    except Exception as exc:
        errors.append(f"agent2: could not read checkpoint CSV: {exc}")

    return {
        **state,
        "web_features": web_features,
        "errors":       errors,
        "current_step": "agent2",
    }