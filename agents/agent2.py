class StateManager:
    """File-based state. Survives crashes. Appends after every entity."""

    def __init__(self):
        self.completed     = set()
        self.audit_entries = []
        self._load()

    def _load(self):
        if os.path.exists(CHECKPOINT_FILE):
            try:
                df = pd.read_csv(CHECKPOINT_FILE)
                self.completed = set(df['canonical_entity'].dropna().tolist())
                print(f"   StateManager: {len(self.completed)} entities done")
            except Exception:
                self.completed = set()
        if os.path.exists(AUDIT_LOG):
            try:
                with open(AUDIT_LOG) as f:
                    self.audit_entries = json.load(f)
            except Exception:
                self.audit_entries = []

    def is_done(self, entity): return entity in self.completed

    def work_queue(self, entity_list):
        q = [e for e in entity_list if e not in self.completed]
        print(f"   Work queue: {len(q)} remaining / {len(entity_list)} total")
        return q

    def save_entity(self, row: dict):
        exists = os.path.exists(CHECKPOINT_FILE)
        with open(CHECKPOINT_FILE, 'a', newline='', encoding='utf-8-sig') as f:
            w = csv.DictWriter(f, fieldnames=row.keys(), quoting=csv.QUOTE_ALL)
            if not exists: w.writeheader()
            w.writerow(row)
        self.completed.add(row.get('canonical_entity', ''))

    def log_validation(self, entry: dict):
        exists = os.path.exists(VALIDATION_LOG)
        with open(VALIDATION_LOG, 'a', newline='', encoding='utf-8-sig') as f:
            w = csv.DictWriter(f, fieldnames=entry.keys(), quoting=csv.QUOTE_ALL)
            if not exists: w.writeheader()
            w.writerow(entry)

    def log_audit(self, entry: dict):
        self.audit_entries.append(entry)
        with open(AUDIT_LOG, 'w', encoding='utf-8') as f:
            json.dump(self.audit_entries, f, indent=2, ensure_ascii=False, default=str)

    def reset(self):
        for f in [CHECKPOINT_FILE, VALIDATION_LOG, AUDIT_LOG]:
            if os.path.exists(f): os.remove(f)
        self.completed = set()
        self.audit_entries = []
        print("   StateManager: reset complete")

print("StateManager defined.")

