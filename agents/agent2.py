def resolve_ig_handle(entity: str, website_ig: str) -> tuple:
    """Returns (handle, source). Website first, DDG fallback."""
    if website_ig:
        return website_ig, "website"

    # DDG fallback
    try:
        from ddgs import DDGS
        _delay(1, 2)
        with DDGS() as ddgs:
            hits = list(ddgs.text(f'"{entity}" site:instagram.com', max_results=5))
        for hit in hits:
            m = re.search(r"instagram\.com/([a-zA-Z0-9_.]{3,30})/?", hit.get("href", ""))
            if m:
                handle = m.group(1).lower()
                return handle, "ddg"
    except Exception:
        pass
    return None, "none"


