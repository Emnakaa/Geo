def scrape_tripadvisor(entity: str) -> dict:
    """DDG snippet parse for TA rating + review count. LLM extracts data."""
    result = {"ta_rating": None, "ta_review_count": None, "ta_url": None}

    _delay(1.5, 2.5)

    try:
        from ddgs import DDGS
        query = f"{entity} restaurant tripadvisor Tunisia"
        with DDGS() as ddgs:
            hits = list(ddgs.text(query, max_results=8))

        # Collect all TA snippets
        ta_snippets = []
        for hit in hits:
            if "tripadvisor.com" not in hit.get("href", ""):
                continue
            if not result["ta_url"]:
                result["ta_url"] = hit["href"]
            snippet = hit.get("body", "")
            title = hit.get("title", "")
            if snippet:
                ta_snippets.append(f"{title} — {snippet}")

        if not ta_snippets:
            print("     TripAdvisor: no snippets found")
            return result

        # LLM extracts rating + review count from snippets
        combined = "\n".join(ta_snippets[:3])
        parsed = call_groq_json(
            f'Extract TripAdvisor rating and review count from these search snippets '
            f'for the restaurant "{entity}":\n\n{combined}\n\n'
            f'Respond ONLY JSON: {{"ta_rating": <float or null>, "ta_review_count": <int or null>}}\n'
            f'If you cannot find a rating or count, use null.'
        )

        if parsed.get("ta_rating") is not None:
            result["ta_rating"] = float(parsed["ta_rating"])
            print(f"     ✓ TA rating: {result['ta_rating']}")
        if parsed.get("ta_review_count") is not None:
            result["ta_review_count"] = int(parsed["ta_review_count"])
            print(f"     ✓ TA reviews: {result['ta_review_count']}")

        if not result["ta_rating"] and not result["ta_review_count"]:
            print("     TripAdvisor: LLM could not extract data from snippets")

    except Exception as e:
        print(f"     TripAdvisor error: {e}")

    return result

