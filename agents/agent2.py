def scrape_serpapi(entity: str, location: str = "Tunisia") -> dict:
    """SerpApi Google Maps. Returns structured venue data."""
    result = {
        "gm_name": None, "gm_rating": None, "gm_review_count": None,
        "gm_address": None, "gm_phone": None, "gm_website": None,
        "gm_category": None, "status": "no_results",
    }
    if not _key_ok(SERPAPI_KEY):
        result["status"] = "no_key"; return result

    try:
        from serpapi import GoogleSearch
        params = {
            "engine": "google_maps",
            "q": f"{entity} restaurant {location}",
            "type": "search",
            "api_key": SERPAPI_KEY,
        }
        search = GoogleSearch(params)
        data = search.get_dict()

        places = data.get("local_results", [])

        # SerpApi sometimes returns a single place instead of a list
        if not places:
            single = data.get("place_results")
            if single:
                places = [single]

        if not places:
            # Retry without "restaurant" keyword
            params["q"] = f"{entity} {location}"
            search = GoogleSearch(params)
            data = search.get_dict()
            places = data.get("local_results", [])
            if not places:
                single = data.get("place_results")
                if single:
                    places = [single]

        if not places:
            print("     SerpApi: no results"); return result

        p = places[0]

        result["gm_name"]         = p.get("title")
        result["gm_rating"]       = p.get("rating")
        result["gm_review_count"] = p.get("reviews")
        result["gm_address"]      = p.get("address")
        result["gm_phone"]        = p.get("phone")
        result["gm_website"]      = p.get("website")

        # Category — handle both str and dict types
        types_raw = p.get("types", [])
        if types_raw and isinstance(types_raw[0], dict):
            result["gm_category"] = ", ".join(t.get("name", "") for t in types_raw)[:80]
        elif types_raw and isinstance(types_raw[0], str):
            result["gm_category"] = ", ".join(types_raw)[:80]
        else:
            result["gm_category"] = p.get("type", "")

        result["status"] = "success"

        print(f"     ✓ Found: {result['gm_name']}")
        print(f"       Rating   : {result['gm_rating']} ({result['gm_review_count']} reviews)")
        print(f"       Category : {result['gm_category']}")
        print(f"       Address  : {result['gm_address']}")
        print(f"       Phone    : {result['gm_phone'] or '(none)'}")
        print(f"       Website  : {result['gm_website'] or '(none)'}")

    except Exception as e:
        print(f"     SerpApi error: {e}")
        result["status"] = f"error: {str(e)[:80]}"

    _delay()
    return result

print("SerpApi defined.")

