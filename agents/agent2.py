def scrape_wikipedia(entity: str) -> bool:
    """Returns True if entity has a Wikipedia page (en or fr)."""
    try:
        import wikipediaapi
        for lang in ["en", "fr"]:
            wiki = wikipediaapi.Wikipedia(user_agent="Agent2-GEO/1.0", language=lang)
            for title in [entity, entity.title(), entity.replace(" ", "_")]:
                if wiki.page(title).exists():
                    print(f"     ✓ Wikipedia ({lang})")
                    return True
        print("     Wikipedia: not found")
    except Exception as e:
        print(f"     Wikipedia error: {e}")
    return False

