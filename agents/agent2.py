def extract_social_from_website(website_url: str) -> dict:
    """Fetch website HTML, extract IG/FB links. No browser needed."""
    socials = {"website_ig": None, "website_fb": None}
    if not website_url: return socials

    try:
        import httpx
        r = httpx.get(website_url, timeout=10, follow_redirects=True,
                      headers={"User-Agent": "Mozilla/5.0"})
        html = r.text

        ig = re.search(r'href=["\']?https?://(?:www\.)?instagram\.com/([A-Za-z0-9_.]{3,30})', html)
        fb = re.search(r'href=["\']?https?://(?:www\.)?facebook\.com/([A-Za-z0-9_.]{3,50})', html)

        if ig: socials["website_ig"] = ig.group(1).lower()
        if fb: socials["website_fb"] = fb.group(1).lower()

        found = []
        if socials["website_ig"]: found.append(f"IG=@{socials['website_ig']}")
        if socials["website_fb"]: found.append(f"FB=@{socials['website_fb']}")
        if found:
            print(f"     ✓ Website socials: {', '.join(found)}")
        else:
            print("     Website: no social links found")

    except Exception as e:
        print(f"     Website fetch error: {str(e)[:60]}")

    return socials

