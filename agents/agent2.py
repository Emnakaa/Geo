import requests as req

def call_groq(prompt: str, system: str = "", max_retries: int = 3) -> str:
    """Single LLM endpoint. Returns raw text."""
    if not _key_ok(GROQ_API_KEY): return ""

    messages = []
    if system: messages.append({"role": "system", "content": system})
    messages.append({"role": "user", "content": prompt})

    for attempt in range(max_retries):
        try:
            r = req.post(
                "https://api.groq.com/openai/v1/chat/completions",
                headers={"Authorization": f"Bearer {GROQ_API_KEY}",
                         "Content-Type": "application/json"},
                json={"model": GROQ_MODEL, "messages": messages,
                      "max_tokens": 600, "temperature": 0.1},
                timeout=30,
            )
            if r.status_code == 429:
                time.sleep((2 ** attempt) * 3); continue
            if r.status_code >= 500:
                if attempt < max_retries - 1: time.sleep(5); continue
                return ""
            if r.status_code != 200: return ""
            return r.json()["choices"][0]["message"]["content"]
        except req.exceptions.Timeout:
            if attempt < max_retries - 1: time.sleep(3); continue
        except Exception as e:
            print(f"     Groq error: {e}"); return ""
    return ""


def _parse_json(text: str) -> dict:
    text = re.sub(r"```(?:json)?\s*", "", text).strip()
    text = re.sub(r",\s*([}\]])", r"\1", text)
    try: return json.loads(text)
    except json.JSONDecodeError: pass
    m = re.search(r"\{.*\}", text, re.DOTALL)
    if m:
        try: return json.loads(m.group())
        except json.JSONDecodeError: pass
    return {}


def call_groq_json(prompt: str, system: str = "") -> dict:
    raw = call_groq(prompt, system)
    if not raw: return {}
    parsed = _parse_json(raw)
    if parsed: return parsed
    # Retry with stricter instruction
    raw = call_groq(prompt + "\n\nRespnd ONLY valid JSON. No markdown, no explanation.", system)
    return _parse_json(raw) if raw else {}

